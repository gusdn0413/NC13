package day0430;
// 기본형 데이터타입 04 논리형
// 논리형 데이터타입은 boolean 이라는 데이터타입만 존재하고 boolean ㄷ이터타입에는딱 2가지 값만 저장 가능하다
// true false
public class Ex10Boolean {
    public static void main(String[] args) {
        // boolean 변수 isTrue 를 선언하고 true 값으로 초기화
        boolean isTrue = true;
        System.out.println(isTrue);
        // boolean 변수 isFalse 를 선언하고 false 값으로 초기화
        boolean isFalse = false;
        System.out.println(isFalse);
    }
}
